class Owner
  attr_accessor :number_of_pets
  attr_reader :name

  def initialize(name)
    @name = name
    @number_of_pets = 0
  end
end

class Shelter

  def initialize
    @adoptions = {}
  end

  def adopt(owner_name, pet_name)
    @adoptions.include?(owner_name) ? @adoptions[owner_name] << pet_name : @adoptions[owner_name] = [pet_name]
    owner_name.number_of_pets += 1
  end

  def print_adoptions
    @adoptions.each do |owner, pets|
      puts "#{owner.name} has adopted the following pets"
        pets.each do |pet|
          puts "a #{pet.pet_type} named #{pet.pet_name}"
        end
      puts "\n"
    end
  end
end



